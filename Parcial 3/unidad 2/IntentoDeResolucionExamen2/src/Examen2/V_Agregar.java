    private void btnCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCodigoActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(null, "Area 1");
        String nombre = txtNombre.getText();
        String edad = txtEdad.getText();
        String correo = txtCorreo.getText();
       
        
       if (nombre.length() != 0 && edad.length() != 0 && correo.length()!=0){
         if (edad.length()>2){
            JOptionPane.showMessageDialog(null, "Edad no valida");
            txtEdad.setText("");
        }else{
         JOptionPane.showMessageDialog(null, "Accedido");
        String codigo = (nombre.substring(0,2)+edad.substring(0,2));
        btnAgregar.setEnabled(true);
        JOptionPane.showMessageDialog(null, "Su codigo es: "+codigo);
        txtCorreo.setEnabled(false);
        txtEdad.setEnabled(false);
        txtNombre.setEnabled(false);
               
         }
        
        
        }else{
           JOptionPane.showMessageDialog(null, "Faltan datos ");
       }
        
    }//GEN-LAST:event_btnCodigoActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
         String nombre = txtNombre.getText();
        String edad = txtEdad.getText();
        String correo = txtCorreo.getText();
        String codigo = (nombre.substring(0,2)+edad.substring(0,2));
        FileWriter archivo = null;
        try {
            archivo = new FileWriter("Codigo.txt", true);
            archivo.write(codigo +" _ "+nombre+"\n");
            archivo.close();
            JOptionPane.showMessageDialog(null, "Registro guardado");
            archivo = new FileWriter("Datos.txt", true);
            archivo.write(codigo +" _ "+nombre+" _ "+edad+" _ "+correo+"\n");
            archivo.close();
            JOptionPane.showMessageDialog(null, "Registro guardado");
            
            M_Ctabla.addRow(new Object[]{codigo,nombre,edad,correo});//se ingresan los datos al contenedor
            Tabla.setModel(M_Ctabla);

        } catch (IOException ex) {
            Logger.getLogger(V_Agregar.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                archivo.close();
            } catch (IOException ex) {
                Logger.getLogger(V_Agregar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        txtCorreo.setText("");
        txtEdad.setText("");
        txtNombre.setText("");
        txtCorreo.setEnabled(true);
        txtEdad.setEnabled(true);
        txtNombre.setEnabled(true);
        btnAgregar.setEnabled(false);
    
    }//GEN-LAST:event_btnAgregarActionPerformed


