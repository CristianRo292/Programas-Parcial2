    private  void Agregar(){
       if (txtNombre.getText().length() == 0 || txtEdad.getText().length() == 0){//se valida que no este basia la caja de texto ðŸ˜ŽðŸ‘€
       JOptionPane.showMessageDialog(null, "Error, Campo basio");
       }else{
        int edad = Integer.parseInt(txtEdad.getText());
        String nombre = txtNombre.getText();
        String registro = nombre + " , "+ Integer.toString(edad);
        lista.addElement(registro);
        listaNombres.setModel(lista);
        txtEdad.setText("");
        txtNombre.setText("");
       } 
    
    }
