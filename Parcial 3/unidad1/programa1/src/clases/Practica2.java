    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        // TODO add your handling code here:
        String []nombres = new String[lista.size()];
        //System.out.println(lista.getElementAt(2)); //muestra el elemento de una poscicion determinada en el arreglo
        //System.out.println(lista.getElementAt(2)); 
        
        for (int i = 0; i < lista.size(); i++) {
            nombres[i] = (String)lista.getElementAt(i);
        }
        
        String aux = "";
       // int a =0;
       // int b = 0;
        for (int i = 0; i < nombres.length; i++) {//[1,23,2,12]
            for (int j = 0; j < nombres.length -1; j++) {
                //a = nombres[j].codePointAt(0);
                //b = nombres[j+1].codePointAt(0);
                
                if(nombres[j].codePointAt(0) < nombres[j+1].codePointAt(0)){
                    
                    aux = nombres[j+1];
                    nombres[j+1] = nombres[j];
                    nombres[j] = aux;
                }
            }
        }
        lista.clear();//borro la lista
        for (int i = 0; i < nombres.length; i++) {
            //System.out.println(num[i]);
            lista.addElement(nombres[i]);
            listaNombres.setModel(lista);
        }
        
    }//GEN-LAST:event_btnOrdenarActionPerformed

